-- ============================================================
-- 秒杀 Lua 脚本
-- ============================================================
-- 这个脚本在 Redis 中执行，保证原子性（要么全做，要么全不做）
--
-- 为什么用 Lua？
-- 1. 原子性：Lua 脚本执行时，Redis 不会执行其他命令，避免并发问题
-- 2. 性能：减少网络往返，一次请求完成多步操作
-- 3. 简单：Lua 语法简单，适合写这种小逻辑
--
-- 参数说明：
-- KEYS[1] = 库存 key，比如 "seckill:stock:1001"（存储剩余库存数量）
-- KEYS[2] = 已购 key，比如 "seckill:bought:1001"（Set，存储已购买的用户ID）
-- ARGV[1] = 用户ID
--
-- 返回值：
-- 0 = 抢购成功
-- 1 = 库存不足
-- 2 = 重复抢购（用户已经抢过）
-- ============================================================

-- 1. 检查库存是否充足
-- redis.call('get', KEYS[1]) 从 Redis 获取库存值
-- tonumber() 把字符串转成数字
local stock = tonumber(redis.call('get', KEYS[1]))

-- 如果库存 key 不存在（活动还没预热）或者库存 <= 0，返回 1（库存不足）
if stock == nil or stock <= 0 then
    return 1
end

-- 2. 检查用户是否已经抢购过
-- SISMEMBER 检查某个元素是否在 Set 中
-- 如果返回 1，说明用户已经抢过了，返回 2（重复抢购）
if redis.call('sismember', KEYS[2], ARGV[1]) == 1 then
    return 2
end

-- 3. 扣减库存
-- DECR 原子递减，返回递减后的值
redis.call('decr', KEYS[1])

-- 4. 标记用户已抢购
-- SADD 向 Set 中添加元素
redis.call('sadd', KEYS[2], ARGV[1])

-- 5. 返回 0，表示抢购成功
return 0
